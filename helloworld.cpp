#include<iostream>
using namespace std;
int main()
{
	cout << "Hello World!\n";  
	
	/*'cout' acts as printf() function in C.It is used to print
	   output on the Console/terminal. */
	
   /* '<<' is called insertion operator. You can use multiple 
	   insertion operators after cout as illustrate below */
	
	cout << "Hello" << " World!"; 
	
	return 0;
}
